#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	int		len;
	char	*dest;
	int		i;

	i = 0;
	len = ft_strlen(src);
	dest = (char *) malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (0);
	while (*src)
		dest[i++] = *src++;
	dest[i] = '\0';
	return (dest);
}
