int	ft_sqrt(int nb)
{
	long	sqrt;

	if (nb <= 0)
	{
		return (0);
	}
	sqrt = 1;
	if (nb >= 1)
	{
		while (sqrt * sqrt <= nb)
		{
			if (sqrt * sqrt == nb)
			{
				return (sqrt);
			}
			sqrt++;
		}
	}
	return (0);
}
